
public class Incremento_Decremento {
	public static void main(String[] args) {

		int i;
		// Soma
		i = 0;

		i = i + 1;
		i += 2;
		i++;
		i++;

		System.out.println(i);

		// Subtração
		i = 0;

		i = i - 1;
		i--;
		i--;
		i--;

		System.out.println(i);
		// Divisao e Multiplicação
		i = 2;
		i++;
		i *= 2;
		i /= 4;
		System.out.println(i);

	}
}

