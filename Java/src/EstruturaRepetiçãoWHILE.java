
public class EstruturaRepetiçãoWHILE {

	public static void main(String[] args) {

		int i = 1, l = 2;
		while (i <= 20) {
			System.out.println("i igual a :" + i + "");

			i++;

		}
		while (l <= 20) {
			System.out.println("l igual a :" + l + "");

			l *= 3;

		}
	}
}

