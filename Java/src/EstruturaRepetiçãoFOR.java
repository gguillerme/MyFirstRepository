
public class EstruturaRepetiçãoFOR {
	public static void main(String[] args) {
		;
		/*
		 * for(valor inicial, condição, incremento){
		 * 
		 * }
		 */
		for (int i = 1; i <= 21; i *= 2) {
			System.out.println("O valor de i: " + i + " ");
		}
		for (int i = 1; i <= 400; i += 2) {
			System.out.println("O valor de i: " + i + " ");
		}

	}
}
