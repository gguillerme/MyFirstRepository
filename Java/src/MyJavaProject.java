public class MyJavaProject {
	public static void main(String[] args) {

		// Variavel int: Um numero inteiro

		int numero = 10;
		System.out.println(numero);

		int numeroB = 10 + 11;
		System.out.println(numeroB);

		int numeroC = 10 + 33;
		System.out.println(numeroC);

		// Variavel float: Apenas um numero depois da virgula

		float number = 10.9f;
		System.out.println(number);

		float numberB = 10.09f;
		float numberC = 10.87f;
		System.out.println(numberB + numberC);

		// Variavel Double = Varios numeros depois da virgula

		Double numberD = 11.11111111;
		System.out.println(numberD);

		// Variavel String: Numeros, Caracteres, Floats e aspas tamb√©m

		String name = "Guilherme da Silva Dutra";
		System.out.println(name);

		String caracteres = "12345";
		System.out.println(caracteres);

		String flutuante = "10.9";
		System.out.println(flutuante);
	}
}

